make_boxed_text_html <- function(x,
                                 size = "standard",
                                 color = "#333333",
                                 background = "transparent",
                                 font_size = "15px",
                                 padding = "5px",
                                 tt_text = NULL,
                                 tt_position = "left",
                                 tt_text_size = NULL,
                                 border_radius = NULL,
                                 v_align = "middle") {
  
  if (!is.null(tt_position) && size == "standard") {
    text_type <- "aria-label"
  } else {
    text_type <- "title"
  }
  
  text_html <- 
    htmltools::tags$span(
      htmltools::HTML(x),
      style = htmltools::css(
        background = background,
        padding = padding,
        color = color,
        `vertical-align` = v_align,
        `font-size` = font_size,
        border = "none",
        `border-radius` = border_radius
      )
    )
  
  if (size == "standard") {
    text_html <-
      text_html %>%
      htmltools::tagAppendAttributes(
        `aria-label` = tt_text,
        `data-balloon-pos` = tt_position,
        `data-balloon-length` = if (!is.null(tt_text_size)) tt_text_size
      )
  } else {
    text_html <- text_html %>% htmltools::tagAppendAttributes(`title` = tt_text)
  }
  
  text_html %>% as.character()
}
